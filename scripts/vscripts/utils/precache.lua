return function(context)
    for _, p in pairs(particles) do
        PrecacheResource("particle", p, context)
    end
    for _, p in pairs(particle_folders) do
        PrecacheResource("particle_folder", p, context)
    end
    for _, p in pairs(sounds) do
        PrecacheResource("soundfile", p, context)
    end
    for _, p in pairs(models) do
        PrecacheResource("model", p, context)
    end
    local npc_units = LoadKeyValues("scripts/npc/npc_units.txt")
    for unit_name, _ in pairs(npc_units) do
        PrecacheUnitByNameSync(unit_name, context, -1)
    end
    local npc_units_custom = LoadKeyValues("scripts/npc/npc_units_custom.txt")
    for unit_name, unit_data in pairs(npc_units_custom) do
        PrecacheUnitByNameSync(unit_name, context, -1)
        if unit_data and type(unit_data) == "table" and unit_data.Model then
            PrecacheResource("model", unit_data.Model, context)
        end
    end
end